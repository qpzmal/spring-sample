	public static String filter(String str, String pathPre) {
		if (str == null || str.trim().length() < 1) {
			return "";
		}
		if(str.length()>1){
			for (String temp : keySet) {
				if (str.indexOf(temp) > -1) {
					str = str.replace(temp, "<img src=\"" + pathPre + map.get(temp) + "\"/>");
				}
			}
		}
		return str;
	}
